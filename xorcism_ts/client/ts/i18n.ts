const DICTS: Record<string, Dict> = { fr, en, de, it, es, pt, zh, ja, ar, ru };
export const SUPPORTED: { code: string; label: string }[] = [
  { code: "fr", label: "FR" },
  { code: "en", label: "EN" },
  { code: "de", label: "DE" },
  { code: "it", label: "IT" },
  { code: "es", label: "ES" },
  { code: "pt", label: "PT" },
  { code: "zh", label: "中文" },
  { code: "ja", label: "日本語" },
  { code: "ar", label: "العربية" },
  { code: "ru", label: "Рус" },
];

// Right-to-left writing languages (document direction forced via dir="rtl").
const RTL_LANGS = new Set<string>(["ar"]);

const STORAGE_KEY = "xorcism_lang";

export function getLang(): string {
  try {
    const stored = localStorage.getItem(STORAGE_KEY);
    if (stored && DICTS[stored]) return stored;
  } catch {
    /* localStorage unavailable */
  }
  // Browser detection: ISO prefix ("fr-FR" → "fr", "zh-CN" → "zh").
  // Any supported language matches; otherwise fallback to English.
  const nav = (navigator.language || "en").toLowerCase();
  const code = nav.split("-")[0];
  return DICTS[code] ? code : "en";
}

let LANG = getLang();

// Exposes the language to the server (message negotiation) via a cookie,
// in addition to the Accept-Language header.
function syncCookie(): void {
  try {
    document.cookie = `xorcism_lang=${LANG}; path=/; max-age=31536000; samesite=strict`;
  } catch {
    /* ignore */
  }
}

export function lang(): string {
  return LANG;
}

export function t(key: string): string {
  // Fallback: current language → English → French → the key itself.
  return DICTS[LANG]?.[key] ?? en[key] ?? fr[key] ?? key;
}

export function setLang(code: string): void {
  if (!DICTS[code]) return;
  try {
    localStorage.setItem(STORAGE_KEY, code);
  } catch {
    /* ignore */
  }
  LANG = code;
  syncCookie(); // the server will see the new language on reload
  location.reload(); // re-translates static + dynamic
}

export function applyTranslations(root: ParentNode = document): void {
  root.querySelectorAll<HTMLElement>("[data-i18n]").forEach((el) => {
    el.textContent = t(el.getAttribute("data-i18n")!);
  });
  root.querySelectorAll<HTMLElement>("[data-i18n-placeholder]").forEach((el) => {
    el.setAttribute("placeholder", t(el.getAttribute("data-i18n-placeholder")!));
  });
  root.querySelectorAll<HTMLElement>("[data-i18n-title]").forEach((el) => {
    el.setAttribute("title", t(el.getAttribute("data-i18n-title")!));
  });
  document.documentElement.lang = LANG;
  document.documentElement.dir = RTL_LANGS.has(LANG) ? "rtl" : "ltr"; // Arabic: right-to-left
}

/** Builds the language <select> (reusable: top bar OR Settings panel). */
export function createLanguageSelect(): HTMLSelectElement {
  const sel = document.createElement("select");
  sel.className = "lang-select";
  sel.title = t("common.language");
  sel.style.cssText =
    "background:var(--bg);border:1px solid var(--border);border-radius:6px;" +
    "color:var(--text);font-size:12px;padding:4px 6px;cursor:pointer";
  SUPPORTED.forEach((o) => {
    const opt = document.createElement("option");
    opt.value = o.code;
    opt.textContent = o.label;
    if (o.code === LANG) opt.selected = true;
    sel.appendChild(opt);
  });
  sel.onchange = () => setLang(sel.value);
  return sel;
}

/** Injects a language selector into the top bar (idempotent). */
export function initLanguageSelector(): void {
  const bar = document.querySelector(".topbar");
  if (!bar || bar.querySelector(".lang-select")) return;
  // The page hosts its own selector (e.g. Settings panel) → no duplicate.
  if (bar.getAttribute("data-lang-external")) return;
  const sel = createLanguageSelect();
  sel.style.marginLeft = "10px";
  // Inserts after the spacer if present, otherwise at the end of the bar
  const spacer = bar.querySelector(".spacer");
  if (spacer) spacer.after(sel);
  else bar.appendChild(sel);
}

/** Shortcut: applies the translations + installs the selector. */
export function initI18n(): void {
  syncCookie(); // aligns the server cookie with the effective language
  applyTranslations();
  initLanguageSelector();
}
